namespace FirstCalculator_;
public class Calculator
{
    public float Sum(float a, float b)
    {
        return a + b;
    }

    public float Divide(float a, float b)
    {
        return a / b;
    }
    public float Multiplication(float a, float b)
    {
        return a * b;
    }
    public float Substract(float a, float b)
    {
        return a - b;
    }
}